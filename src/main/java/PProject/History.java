package PProject;

import java.util.Date;

public class History {

	private int h_id;
	private String h_userId;
	private int h_recruitId;
	private Date h_time;
	
	
	public int getH_id() {
		return h_id;
	}
	public void setH_id(int h_id) {
		this.h_id = h_id;
	}
	public String getH_userId() {
		return h_userId;
	}
	public void setH_userId(String h_userId) {
		this.h_userId = h_userId;
	}
	public int getH_recruitId() {
		return h_recruitId;
	}
	public void setH_recruitId(int h_recruitId) {
		this.h_recruitId = h_recruitId;
	}
	public Date getH_time() {
		return h_time;
	}
	public void setH_time(Date h_time) {
		this.h_time = h_time;
	}

	
	
}
