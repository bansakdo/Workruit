package PProject;

public class User {

	private String u_id;
	private String u_passwd;
	private String u_name;
	private String u_level;
	private String u_email;
	private String u_tel;
	private String u_etc;
	private String u_historyId;
	
	
	
	public String getU_id() {
		return u_id;
	}
	public void setU_id(String u_id) {
		this.u_id = u_id;
	}
	public String getU_passwd() {
		return u_passwd;
	}
	public void setU_passwd(String u_passwd) {
		this.u_passwd = u_passwd;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public String getU_level() {
		return u_level;
	}
	public void setU_level(String u_level) {
		this.u_level = u_level;
	}
	public String getU_email() {
		return u_email;
	}
	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
	public String getU_tel() {
		return u_tel;
	}
	public void setU_tel(String u_tel) {
		this.u_tel = u_tel;
	}
	public String getU_etc() {
		return u_etc;
	}
	public void setU_etc(String u_etc) {
		this.u_etc = u_etc;
	}
	public String getU_historyId() {
		return u_historyId;
	}
	public void setU_historyId(String u_historyId) {
		this.u_historyId = u_historyId;
	}

	
	
}
